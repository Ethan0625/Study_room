def cutTheSticks(arr):
    # Write your code here
    stick = arr
    cut = min(stick)
    result = []
    while stick:
        result.append(len(stick))
        stick = [i-cut for i in stick if i>cut]
        if len(stick)==0:
            break
        cut = min(stick)
    return result
    
