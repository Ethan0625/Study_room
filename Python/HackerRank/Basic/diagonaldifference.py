def diagonalDifference(arr):
    # Write your code here
    shape = len(arr)
    x_val=0
    y_val=0
    for i in range(shape):
        x_val+=arr[i][i]
        y_val+=arr[i][-(i+1)]
    return abs(x_val-y_val)
    
    
